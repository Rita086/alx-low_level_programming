#include <stdio.h>
/**
 * add - sums two integers
 * @a: first int
 * @b: second int 
 * Return: The sum of the integers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substracts two integers
 * @a: first int
 * @b: second int
 * Return: The subtraction
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * div - divides two integers
 * @a: first int
 * @b: second int
 * Return: The division of the integers
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: The final result
 */
int mod(int a, int b)
{
        return (a % b);
}
/**
 * mul - multiples two integers
 * @a: first int
 * @b: second int
 * Return: The multiplication of the integers
 */
int mul(int a, int b)
{
        return (a * b);
}
